cleanup() {
  rm -rf "$WORKDIR"
}
trap cleanup EXIT

curl_base() {
  curl "${CURL_PROXY_BYPASS[@]}" "$@"
}

json_get() {
  local json="$1"
  local path="$2"
  printf '%s' "$json" | node -e '
    const fs = require("fs");
    const input = fs.readFileSync(0, "utf8");
    const obj = JSON.parse(input);
    const path = process.argv[1].split(".");
    let cur = obj;
    for (const part of path) {
      if (!part) continue;
      if (cur == null) {
        process.exit(2);
      }
      if (/^\d+$/.test(part)) {
        cur = cur[Number(part)];
      } else {
        cur = cur[part];
      }
    }
    if (cur === undefined) {
      process.exit(2);
    }
    if (typeof cur === "object") {
      process.stdout.write(JSON.stringify(cur));
    } else {
      process.stdout.write(String(cur));
    }
  ' "$path"
}

assert_eq() {
  local desc="$1"
  local actual="$2"
  local expected="$3"
  if [[ "$actual" == "$expected" ]]; then
    echo "PASS: $desc"
    PASS=$((PASS + 1))
  else
    echo "FAIL: $desc (expected '$expected', got '$actual')"
    FAIL=$((FAIL + 1))
  fi
}

assert_contains() {
  local desc="$1"
  local haystack="$2"
  local needle="$3"
  if [[ "$haystack" == *"$needle"* ]]; then
    echo "PASS: $desc"
    PASS=$((PASS + 1))
  else
    echo "FAIL: $desc (missing '$needle')"
    FAIL=$((FAIL + 1))
  fi
}

assert_file_nonempty() {
  local desc="$1"
  local file_path="$2"
  if [[ -s "$file_path" ]]; then
    echo "PASS: $desc"
    PASS=$((PASS + 1))
  else
    echo "FAIL: $desc (file missing or empty: $file_path)"
    FAIL=$((FAIL + 1))
  fi
}

fetch_csrf_token() {
  local cookie_jar="$1"
  curl_base -s -b "$cookie_jar" -c "$cookie_jar" "$BASE_URL/api/v1/auth/me" >/dev/null
  awk '$6 == "XSRF-TOKEN" { print $7 }' "$cookie_jar" | tail -n 1
}

api_json() {
  local method="$1"
  local url="$2"
  local cookie_jar="$3"
  local csrf_token="$4"
  local body="$5"
  local response=""
  local attempt
  for attempt in $(seq 1 6); do
    response="$(curl_base --max-time 20 -sS \
      -X "$method" "$url" \
      -b "$cookie_jar" \
      -c "$cookie_jar" \
      -H "X-XSRF-TOKEN: $csrf_token" \
      -H "Content-Type: application/json" \
      -d "$body")"
    if [[ "$response" != *"\"code\":429"* ]]; then
      printf '%s' "$response"
      return 0
    fi
    sleep "$attempt"
  done
  printf '%s' "$response"
}

create_skill_zip() {
  local output_zip="$1"
  local name="$2"
  local description="$3"
  local version="$4"
  local extra_frontmatter="${5:-}"
  local pkg_dir="$WORKDIR/$(basename "$output_zip" .zip)"
  mkdir -p "$pkg_dir"
  cat >"$pkg_dir/SKILL.md" <<EOF
---
name: $name
description: $description
version: $version
$extra_frontmatter---

# $name

Generated by enterprise AgentHub smoke test.
EOF
  cat >"$pkg_dir/README.md" <<EOF
# $name

Generated by enterprise AgentHub smoke test.
EOF
  (cd "$pkg_dir" && zip -q -r "$output_zip" SKILL.md README.md)
}

publish_skill() {
  local namespace_slug="$1"
  local cookie_jar="$2"
  local csrf_token="$3"
  local zip_path="$4"
  curl_base --max-time 60 -sS \
    -X POST "$BASE_URL/api/v1/skills/$namespace_slug/publish" \
    -b "$cookie_jar" \
    -c "$cookie_jar" \
    -H "X-XSRF-TOKEN: $csrf_token" \
    -F "file=@$zip_path;type=application/zip" \
    -F "visibility=PUBLIC" \
    -F "confirmWarnings=true"
}

wait_for_search_hit() {
  local expected_slug="$1"
  local query_string="$2"
  local cookie_jar="${3:-}"
  for attempt in $(seq 1 20); do
    local response
    if [[ -n "$cookie_jar" ]]; then
      response="$(curl_base --max-time 20 -sS -b "$cookie_jar" "$BASE_URL/api/v1/search/skills?$query_string")"
    else
      response="$(curl_base --max-time 20 -sS "$BASE_URL/api/v1/search/skills?$query_string")"
    fi
    if printf '%s' "$response" | grep -q "\"slug\":\"$expected_slug\""; then
      return 0
    fi
    sleep "$attempt"
  done
  return 1
}

