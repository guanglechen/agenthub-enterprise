PASS=0
FAIL=0
BASE_HOST="$(printf '%s' "$BASE_URL" | sed -E 's#^[a-zA-Z]+://([^/:]+).*$#\1#')"
CURL_PROXY_BYPASS=()

if [[ "$BASE_HOST" == "localhost" || "$BASE_HOST" == "127.0.0.1" || "$BASE_HOST" == "::1" ]]; then
  CURL_PROXY_BYPASS=(--noproxy "*")
fi

